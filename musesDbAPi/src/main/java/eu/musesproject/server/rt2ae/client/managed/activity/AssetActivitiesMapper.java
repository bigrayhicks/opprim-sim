public class AssetActivitiesMapper extends AssetActivitiesMapper_Roo_Gwt {

    public AssetActivitiesMapper(ApplicationRequestFactory factory, PlaceController placeController) {
        this.factory = factory;
        this.placeController = placeController;
    }

    public Activity getActivity(ProxyPlace place) {
        switch(((place.getOperation()))) {
            case DETAILS:
                return new AssetDetailsActivity((EntityProxyId<AssetProxy>) place.getProxyId(), factory, placeController, ScaffoldApp.isMobile() ? AssetMobileDetailsView.instance() : AssetDesktopDetailsView.instance());
            case EDIT:
                return makeEditActivity(place);
            case CREATE:
                return makeCreateActivity();
        }
        throw new IllegalArgumentException("Unknown operation " + place.getOperation());
    }
}
