package eu.muses.sim.userman.action;

public abstract class UserAction {

	private int id;
	
	public UserAction() {
		super();
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	
}
